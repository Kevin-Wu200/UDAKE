export function translate_company(language: AppLanguage, key: string, params?: Record<string, any>): string {
    let text = messages[language][key] ?? key;
    if (params) {
        Object.keys(params).forEach(p => {
            text = text.replace(new RegExp(`\\{${p}\\}`, 'g'), params[p]);
        });
    }
    return text;
}
